module.exports=require('apps/drawapp').extend({
tools:{Rect:{
col:'red',
id:0,
borderRadius:20,
shadowOffset:[5,5],
borderWidth:2,
pixelStyle:function(){$
this.borderColor=mix('white','black',this.mesh.y)
this.color=mix('white',this.color,abs(
sin(sin(this.time+this.id)*8.*length(
this.mesh.xy-vec2(.5+0.*sin(this.time+8.*this.id))
)+this.time)
))
}
}},
onFingerDown:function(){
this.redraw()
},
onDraw:function(){
for(var i=0;i<200;i++){
this.drawRect({
id:i*0.01,
color:[random(),random(),random(),1],
x:(i%10)*100,
y:floor(i/10)*100,
w:100,
h:100
})
}
}
})